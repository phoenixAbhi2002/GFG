class Solution
{
    public:
    //Function to rotate a linked list.
    Node* rotate(Node* head, int k)
    {
        // Your code here
        Node* curr=head;
        Node* prev=head;
        Node* last=head;
        
        for(int i=0;i<k;i++){
            curr=curr->next;
        }
        
        if(curr==NULL){
            return head;
        }
        
        for(int i=0;i<k-1;i++){
            prev=prev->next;
        }
        
        while(last->next!=NULL){
            last=last->next;
        }
        last->next=head;
        prev->next = NULL;
        
        return curr;
    }
};
    


